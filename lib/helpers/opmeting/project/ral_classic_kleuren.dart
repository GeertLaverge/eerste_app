  static Iterable<String> zoek(String invoer) {
    final zoektekst = invoer.trim().toLowerCase();
    if (zoektekst.isEmpty) {
      return alle.take(20);
    }

    final woorden = zoektekst
        .split(RegExp(r'\s+'))
        .where((woord) => woord.isNotEmpty)
        .toList(growable: false);

    return alle
        .where((kleur) {
          final bron = kleur.toLowerCase();
          return woorden.every(bron.contains);
        })
        .take(30);
  }
